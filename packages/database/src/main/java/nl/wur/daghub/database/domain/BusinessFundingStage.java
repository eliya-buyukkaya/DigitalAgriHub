package nl.wur.daghub.database.domain;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Table;
import javax.validation.constraints.NotNull;

import lombok.Getter;

@Entity
@Table(name = "business_funding_stages")
public class BusinessFundingStage {
    private @Getter @Id @GeneratedValue(strategy = GenerationType.IDENTITY) int id;
    private @Getter @NotNull String description;
    // private @OneToMany(fetch = FetchType.LAZY, targetEntity = Organisation.class, mappedBy = "businessFundingStage") Set<Organisation> organisations;

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((description == null) ? 0 : description.hashCode());
        result = prime * result + id;
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        BusinessFundingStage other = (BusinessFundingStage) obj;
        if (description == null) {
            if (other.description != null)
                return false;
        } else if (!description.equals(other.description))
            return false;
        if (id != other.id)
            return false;
        return true;
    }

}
