package nl.wur.daghub.database.domain;

import java.util.Set;

import javax.persistence.Entity;
import javax.persistence.FetchType;
import javax.persistence.Id;
import javax.persistence.OneToMany;
import javax.persistence.Table;
import javax.validation.constraints.NotNull;

import lombok.Getter;

@Entity
@Table(name = "countries")
public class Country {
    private @Getter @Id String id;
    private @Getter @NotNull String description;
    private @Getter @NotNull boolean lmic;
    private @OneToMany(fetch = FetchType.LAZY, targetEntity = Region.class, mappedBy = "country") Set<Region> regions;
    // private @OneToMany(fetch = FetchType.LAZY, targetEntity = Organisation.class, mappedBy = "hqcountry") Set<Organisation> organisations;
    // private @ManyToMany(fetch = FetchType.LAZY, targetEntity = Solution.class, mappedBy = "countries") Set<Solution> solutions;

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((id == null) ? 0 : id.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        Country other = (Country) obj;
        if (id == null) {
            if (other.id != null)
                return false;
        } else if (!id.equals(other.id))
            return false;
        return true;
    }

}
