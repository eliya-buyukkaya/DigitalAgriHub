import io.swagger.v3.oas.annotations.info.Info;
import io.swagger.v3.oas.annotations.security.SecurityRequirement;
import io.swagger.v3.oas.annotations.security.SecurityScheme;

@Configuration
@SecurityScheme(name = "DataEntryAuthBearer", scheme = "bearer", type = SecuritySchemeType.HTTP)
@SecurityScheme(name = "DataEntryAuthBasic", scheme = "basic", type = SecuritySchemeType.HTTP)
@OpenAPIDefinition(info = @Info(title = "DataEntry API"), security = @SecurityRequirement(name = "DataEntryAuthBearer"))
public class OpenApiConfig {
}
