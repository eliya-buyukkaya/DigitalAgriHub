package nl.wur.dataentry.dto;

import javax.validation.constraints.NotNull;
import javax.validation.constraints.Size;

import lombok.AllArgsConstructor;
import lombok.Getter;
import lombok.NoArgsConstructor;
import nl.wur.dataentry.validation.PasswordMatches;
import nl.wur.dataentry.validation.ValidPassword;

@PasswordMatches
@NoArgsConstructor
@AllArgsConstructor
public class DtoUserPassword {
    protected @Getter @NotNull @Size(min = 1, message = "Length must be greater than {min}") @ValidPassword String password;
    protected @Getter @NotNull @Size(min = 1) String matchingPassword;    
}
